# Fase 07 - Operadores Aritméticos
# Teoria
# Além do operador aritmético de adição visto na aula Fase06,
# temos outros operadores aritméticos.

# Adição
print('Adição ( + )')
a = 5 + 2
print('5 + 2 =', a)
print('')

# Subtração
print('Subtração ( - )')
s = 5 - 2
print('5 - 2 =', s)
print('')

# Multiplicação
print('Multiplicação ( * )')
m = 5 * 2
print('5 * 2 =', m)
print('')

# Divisão
print('Divisão ( / )')
d = 5 / 2
print('5 / 2 =', d)
print('')

# Potência ou Exponenciação
print('Exponenciação ( ** )')
e = 5**2
print('5**2 =', e)
print('')

# Divisão Inteira
print('Divisão Inteira ( // )')
di = 5 // 2
print('5 // 2 =', di)
print('')

# Resto da divisão ou Módulo
print('Módulo ( % )')
mod = 5 % 2
print('5 % 2 =', mod)
print('')
