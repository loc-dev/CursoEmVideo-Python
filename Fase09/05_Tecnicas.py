# Técnica de Fatiamento

# Utilizando a variável 'frase'
frase = 'Curso em Vídeo Python'
print('Técnica de Fatiamento')
print('Está será a frase de exemplo: ', frase)
print('')

print(frase[3])
print(frase[3:13])
print(frase[:13])
print(frase[13:])
print(frase[1:15])
print(frase[1:15:2])
print(frase[1::2])
print(frase[::2])
